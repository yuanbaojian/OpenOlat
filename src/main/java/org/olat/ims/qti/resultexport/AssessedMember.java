package org.olat.ims.qti.resultexport;

public class AssessedMember {
	
	private String username;
	private String name;
	private String lastname;
	private String firstname;
	private String email;
	private String tries;
	
	public AssessedMember(){}



	public AssessedMember(String username, String lastname, String firstname, String email, String tries) {
		super();
		this.username = username;
		this.lastname = lastname;
		this.firstname = firstname;
		this.email = email;
		this.tries = tries;
	}
	


	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getLastname() {
		return lastname;
	}

	public void setLastname(String lastname) {
		this.lastname = lastname;
	}

	public String getFirstname() {
		return firstname;
	}

	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getTries() {
		return tries;
	}

	public void setTries(String tries) {
		this.tries = tries;
	}
	

}
