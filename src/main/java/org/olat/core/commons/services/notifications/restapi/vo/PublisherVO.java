package org.olat.core.commons.services.notifications.restapi.vo;

import java.util.ArrayList;
import java.util.List;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlElementWrapper;
import javax.xml.bind.annotation.XmlRootElement;

import org.olat.core.commons.services.notifications.Publisher;
import org.olat.user.restapi.UserVO;

/**
 * 
 * Initial date: 20.01.2016<br>
 * @author srosse, stephane.rosse@frentix.com, http://www.frentix.com
 *
 */
@XmlAccessorType(XmlAccessType.FIELD)
@XmlRootElement(name = "subscribersVO")
public class PublisherVO {
	
	//publisher data
	private String type;
	private String data;
	private String businessPath;
	
	//subscription context
	private String resName;
	private Long resId;
	private String subidentifier;
	//list of users to subscribe
	@XmlElementWrapper(name="users")
	@XmlElement(name="user")
	private List<UserVO> users = new ArrayList<>();
	
	public PublisherVO() {
		//
	}
	
	public PublisherVO(Publisher publisher) {
		type = publisher.getType();
		data = publisher.getData();
		businessPath = publisher.getBusinessPath();
		resId = publisher.getResId();
		resName = publisher.getResName();
		subidentifier = publisher.getSubidentifier();
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public String getBusinessPath() {
		return businessPath;
	}

	public void setBusinessPath(String businessPath) {
		this.businessPath = businessPath;
	}

	public String getResName() {
		return resName;
	}

	public void setResName(String resName) {
		this.resName = resName;
	}

	public Long getResId() {
		return resId;
	}

	public void setResId(Long resId) {
		this.resId = resId;
	}

	public String getSubidentifier() {
		return subidentifier;
	}

	public void setSubidentifier(String subidentifier) {
		this.subidentifier = subidentifier;
	}

	public List<UserVO> getUsers() {
		return users;
	}

	public void setUsers(List<UserVO> users) {
		this.users = users;
	}
}
